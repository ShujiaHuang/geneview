class TestCreateFigure:
    """Tests for create_figure helper."""

    def test_default_figsize(self):
        """Should create figure with default size when args.figsize is None."""
        import argparse
        args = argparse.Namespace(figsize=None, facecolor="w")
        fig, ax = create_figure(args, default_figsize=(10, 5))
        w, h = fig.get_size_inches()
        assert abs(w - 10) < 0.01
        assert abs(h - 5) < 0.01

    def test_custom_figsize(self):
        """Should use custom figure size from args."""
        import argparse
        args = argparse.Namespace(figsize=[8, 3], facecolor="w")
        fig, ax = create_figure(args)
        w, h = fig.get_size_inches()
        assert abs(w - 8) < 0.01
        assert abs(h - 3) < 0.01


